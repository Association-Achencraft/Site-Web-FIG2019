Conçu par Jacques André, ce projet de reconstruction - labellisé « Patrimoine XXème » - est composé d’alignements
d’immeubles urbains mitoyens, de 18,5 m de hauteur, construits sur le modèle suivant :<br>
- commerces en rez-de-chaussée ;<br>
- trois étages d’habitation, avec balcons au 1er et 3ème étage ;<br>
- grenier, parfois aménagé en habitation.<p>
Cette répétition, tout au long des rues, crée un effet visuel saisissant, moderne et monumental.<br>
Aujourd’hui, cet ensemble remarquable comporte :<br>
- 356 logements (dont 9 qualifiés de locaux commerciaux), dont 75 vacants ;<br>
- 90 commerces, dont 16 vacants.

