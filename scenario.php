<h3>Scénario de l'atelier</h3><p><p>


Chaque ilot a été conçu et réalisé de façon ouverte sur les rues adjacentes et les coeurs d’ilots ont toujours été
occupés, de façon « anarchique », par des jardins, garages, hangars...<p>
Comment reconquérir ces espaces (coeurs d’ilots), pour revitaliser le centre-ville, en appréhendant au mieux et de
façon transversale les problématiques de mobilité, de logement, d’économie, d’alimentation locale, de gestion des
ressources, d’énergie… ?<br>
Tous les acteurs du territoire sont ainsi conviés à inventer leur avenir !<p><p>

Chaque participant, présent sur le site du festival, sera pris en charge par les animateurs de l’atelier afin de
parfaitement comprendre le contexte et le territoire, de découvrir l’outil MINECRAFT et de construire son propre projet.<br>
Il pourra également visiter un coeur d’ilot, à 5 minutes de marche de l’Espace Géo-numérique, accompagné par un
animateur de l’atelier.<br>
L’atelier est réservé aux participant(e)s de plus de 10 ans et la durée moyenne de jeu est d’environ 30 minutes.<p><p>
Toute personne, non présente sur le site du festival, pourra également participer à distance à l’atelier et sera prise en
charge par des animateurs dédiés.<br>
La session de construction se déroulera ainsi du vendredi matin au dimanche midi.<p><p>

